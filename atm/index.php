        <div class="h4">ANDREAS TRANSØ Music</div>

<p class="normal"><img src="images/2.jpg" width="220" height="145" hspace="10" vspace="10" align="right" />Andreas Transø is a modern embodiment of the old seanchaí (shanachie) tradition, a traveling musician and educator keeping alive traditional folk culture and history while captivating and entertaining his audiences, large and small, whatever and wherever the venue. Transø was born and raised in the Ocooch Mountains of southwestern Wisconsin, a remote and beautiful area of the upper Driftless Region that was the final destination for many Irish immigrants, especially during the 19th century. Traditional Irish tunes and songs, along with a love for storytelling and good humor, were passed down to Andreas, and he is now bringing them to audiences both big and small.&nbsp;        </p>
